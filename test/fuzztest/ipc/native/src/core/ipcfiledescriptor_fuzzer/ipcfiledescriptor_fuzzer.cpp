#include "ipcfiledescriptor_fuzzer.h"
#include "ipc_file_descriptor.h"
#include "message_parcel.h"

namespace OHOS {
void IPCFileDescriptorFuzzTest(const uint8_t *data, size_t size)
{
    if (data == nullptr || size == 0) {
        return;
    }

    MessageParcel parcel;
    parcel.WriteBuffer(data, size);
    int fd = parcel.ReadInt32();

    IPCFileDescriptor fileDesc(fd);
}

void SetFdFuzzTest(const uint8_t *data, size_t size)
{
    if (data == nullptr || size == 0) {
        return;
    }

    MessageParcel parcel;
    parcel.WriteBuffer(data, size);
    int fd = parcel.ReadInt32();
    IPCFileDescriptor fileDesc;

    fileDesc.SetFd(fd);
}
} // namespace OHOS

/* Fuzzer entry point */
extern "C" int LLVMFuzzerTestOneInput(const uint8_t *data, size_t size)
{
    /* Run your code on data */
    OHOS::IPCFileDescriptorFuzzTest(data, size);
    OHOS::SetFdFuzzTest(data, size);
    return 0;
}
